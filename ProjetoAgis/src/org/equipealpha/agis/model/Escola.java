package org.equipealpha.agis.model;

public class Escola {
    
   private int id_escola; 
   private String nome;

    public Escola() {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId_escola() {
        return id_escola;
    }

    public void setId_escola(int id_escola) {
        this.id_escola = id_escola;
    }
    
}
